package com.exa.pesa.core.views.checkpoint;

public class LogbookCreationView {
    private String inputDate;
    private String name;
    private String inputReasonId;
    private String materialTypeId;
    private String vehicleTypeId;
    private String vehiclePlate;
    private String vehicleColorId;
    private String contactPersonId;
    private String areaId;
    private String observation;

    public LogbookCreationView() {
        super();
    }

    public String getInputDate() {
        return inputDate;
    }

    public void setInputDate(String inputDate) {
        this.inputDate = inputDate;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getInputReasonId() {
        return inputReasonId;
    }

    public void setInputReasonId(String inputReasonId) {
        this.inputReasonId = inputReasonId;
    }

    public String getMaterialTypeId() {
        return materialTypeId;
    }

    public void setMaterialTypeId(String materialTypeId) {
        this.materialTypeId = materialTypeId;
    }

    public String getVehicleTypeId() {
        return vehicleTypeId;
    }

    public void setVehicleTypeId(String vehicleTypeId) {
        this.vehicleTypeId = vehicleTypeId;
    }

    public String getVehiclePlate() {
        return vehiclePlate;
    }

    public void setVehiclePlate(String vehiclePlate) {
        this.vehiclePlate = vehiclePlate;
    }

    public String getVehicleColorId() {
        return vehicleColorId;
    }

    public void setVehicleColorId(String vehicleColorId) {
        this.vehicleColorId = vehicleColorId;
    }

    public String getContactPersonId() {
        return contactPersonId;
    }

    public void setContactPersonId(String contactPersonId) {
        this.contactPersonId = contactPersonId;
    }

    public String getAreaId() {
        return areaId;
    }

    public void setAreaId(String areaId) {
        this.areaId = areaId;
    }

    public String getObservation() {
        return observation;
    }

    public void setObservation(String observation) {
        this.observation = observation;
    }
}
